// Вивести таблицю з 3 рядків і 7 стовпців

if (confirm('Почати тестування?')) {

	let row = '<tr>'

	for (let j = 1; j <= 3; j++) {
		row += '<tr>'
		for (let i = 1; i <= 7; i++) {
			row += `<td>${i}</td>`
		}
		row += '</tr>'
	}
	document.write(row)

}
